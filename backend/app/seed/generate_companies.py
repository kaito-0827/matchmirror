# ---------------------------------------------------------------------------
def build() -> list[dict[str, Any]]:
    companies: list[dict[str, Any]] = []
    n = 0
    for ind_i, industry in enumerate(INDUSTRIES):
        for slot in range(5):  # 業界ごと5社
            n += 1
            # --- 各軸をローテーション配分（業界とずらして相関を断つ）---
            size_label, lo, hi = SIZE_BANDS[(ind_i + slot) % len(SIZE_BANDS)]
            region = REGIONS[(ind_i * 5 + slot) % len(REGIONS)]
            leave_ease = LEAVE_EASE[(ind_i + slot * 2) % len(LEAVE_EASE)]
            ot_label, ot_hours_base, ot_desc = OVERTIME_BANDS[(ind_i * 2 + slot) % len(OVERTIME_BANDS)]
            ot_hours = ot_hours_base + random.randint(-3, 5)

            # 働き方: 現場主体業界は出社/シフト寄りに補正しつつ、一部は本社系で在宅も
            if industry in ONSITE_HEAVY and slot < 3:
                remote = random.choice(["シフト・現場勤務", "原則出社"])
            else:
                remote = REMOTE_POLICIES[(ind_i + slot * 3) % len(REMOTE_POLICIES)]

            autonomy = ["高", "中", "低"][(ind_i + slot) % 3]

            # 文化タグ2つ
            t1 = CULTURE_TAGS[(ind_i + slot) % len(CULTURE_TAGS)]
            t2 = CULTURE_TAGS[(ind_i * 3 + slot + 5) % len(CULTURE_TAGS)]
            if t2 == t1:
                t2 = CULTURE_TAGS[(CULTURE_TAGS.index(t1) + 1) % len(CULTURE_TAGS)]
            culture_text = f"{CULTURE_TEMPLATES[t1]} {CULTURE_TEMPLATES[t2]}"

            job_title = JOB_TITLES[industry][slot]
            employees = random.randint(lo, hi)
            founded = {
                "スタートアップ": random.randint(2016, 2023),
                "中小企業": random.randint(1998, 2018),
                "中堅企業": random.randint(1985, 2010),
                "大企業": random.randint(1960, 2000),
                "超大手": random.randint(1920, 1990),
            }[size_label]

            # OJT文
            ojt_t = random.choice(OJT_TEMPLATES)
            ojt = ojt_t.format(
                m=random.choice(["1ヶ月", "2週間", "3ヶ月", "半年"]),
                mentor=random.choice(OJT_MENTOR),
                freq=random.choice(OJT_FREQ),
                detail=random.choice(OJT_DETAIL),
            )

            cid = f"company-{n:03d}"
            jid = f"job-{n:03d}"
            rec = {
                "company_id": cid,
                "job_id": jid,
                "metadata": {
                    "name": make_company_name(industry, slot),
                    "industry": industry,
                    "employee_count": employees,
                    "size_band": size_label,
                    "region": region,
                    "founded_year": founded,
                    "remote_policy": remote,
                    "overtime_band": ot_label,
                    "leave_ease": leave_ease,
                    "culture_tags": [t1, t2],
                },
                "job_title": job_title,
                "daily_tasks": make_daily_tasks(job_title, autonomy),
                "ojt_structure": ojt,
                "leave_reality": make_leave_reality(ot_label, ot_hours, ot_desc, leave_ease),
                "culture_values": culture_text,
                "evaluation_criteria": random.choice(EVAL_TEMPLATES),
                "workstyle": make_workstyle(remote, ot_label, region),
            }
            companies.append(rec)
    return companies


def main() -> None:
    companies = build()
    assert len(companies) == 100, f"expected 100, got {len(companies)}"
    # 社名の重複チェック
    names = [c["metadata"]["name"] for c in companies]
    assert len(set(names)) == len(names), "company names must be unique"
    with open(OUT_PATH, "w", encoding="utf-8") as f:
        json.dump(companies, f, ensure_ascii=False, indent=2)

    # 偏りチェック用の簡易サマリーを出力
    from collections import Counter
    def dist(key_fn):
        return dict(Counter(key_fn(c) for c in companies))
    print(f"✓ {len(companies)}社を生成 -> {OUT_PATH}")
    print("  業界:", dist(lambda c: c["metadata"]["industry"]))
    print("  規模:", dist(lambda c: c["metadata"]["size_band"]))
    print("  地域:", dist(lambda c: c["metadata"]["region"]))
    print("  働き方:", dist(lambda c: c["metadata"]["remote_policy"]))
    print("  残業:", dist(lambda c: c["metadata"]["overtime_band"]))
    print("  有休:", dist(lambda c: c["metadata"]["leave_ease"]))


if __name__ == "__main__":
    main()
